package in.co.rays;

public class switchcalcu {
	
public static void main(String[]args) {
	int a= 45;
	int b= 35;
	
	char c = '*';
	
	switch (c) {
	
	case '+':
		int sum = (a+b);
		System.out.println(sum);
		
		break;
		
	case '-' :
		int sub = (a-b);
		System.out.println(sub);
		
		break;
		
	case '*' :
		int multi = (a*b);
		
		System.out.println(multi);
		
		break;
		
	
	
	
	}
		
		
}
}
